let currentIndex = 0;


const displayState = {
    displayableCards: 15,
    addedItems:0,
    setDisplayableCards: function (number){
    this.displayableCards += number
    }
}

const firstPerfumes = perfumes.slice(1, displayState.displayableCards);


//hero-section

logoImg.src = 'images/aura-logo.png';
function updateImage() {
    img.src = String(perfumeAdverts[currentIndex]?.image);
    overlay.textContent = perfumeAdverts[currentIndex].description;
}

const playPrev = () => {
    currentIndex = (currentIndex - 1 + perfumeAdverts.length) % perfumeAdverts.length;
    updateImage();
}
const playNext = () => {
    currentIndex = (currentIndex + 1) % perfumeAdverts.length;
    updateImage();
}
// Event listeners for navigation
prevBtn.addEventListener('click', () => {
    clearInterval(intervalId);
    playPrev();
});

nextBtn.addEventListener('click', () => {
    clearInterval(intervalId);
    playNext();
});


updateImage();

intervalId = setInterval(() => {
    playNext();
},7000);


